/* Taking INPUT in Arrays */
// =================================================
void main()
{
    // Taking size
    int len;
    printf("Enter size of array : ");
    scanf("%d", &len);

    // Taking array
    int arr[50];
    printf("Enter elements of array : ");
    for (int i = 0; i < len; i++)
        scanf("%d", &arr[i]);
}

