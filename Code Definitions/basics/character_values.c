/* Character Values */
// =================================================
#include<stdio.h>
void main()
{
    printf("Uppercase Characters :\n");
    for (int c=65;c<=90;c++)
        printf("%c ",c);

    printf("\n\nLowercase Characters :\n");
    for (int c=97;c<=122;c++)
        printf("%c ",c);
}
