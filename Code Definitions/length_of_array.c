// Length of Array
// --------------------------------------
// Finds the length of the array using sizeof

#include<stdio.h>

// Main function
void main()
{
    // Initialize variables
    char arr[] = {2,3,5,1,6,4};
    int len = sizeof(arr);

    // Printing the length
    printf("Length of Array : %d", len);
}

