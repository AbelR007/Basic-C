/* Bitwise AND and OR operations */
// =========================================================================
#include<stdio.h>
#include<conio.h>

/*  Definition :
When corresponding two numbers are compared with AND or OR Bitwise Operations, then,
the numbers containing one are resulted out and similarly for both AND and OR operations. */

void main()
{
    int a = 17, b = 6;
    printf("\n %d AND %d = %d", a, b, a & b);
    printf("\n %d OR %d = %d", a, b, a | b);
}
