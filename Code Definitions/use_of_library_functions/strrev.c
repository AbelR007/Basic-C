/* Reversing string using Strrev */
// =================================================
#include<stdio.h>
#include<string.h>
int main()
{
   char str[50] = "Pallotti";
   printf("The given string is =%s\n",str);
   printf("After reversing string is =%s",strrev(str));
   return 0;
}
